'''
Integers (int): whole numbers (eg. 1,2,3)
Float (float): decimal numbers (eg. 2.14, 10.11, 3.4)
String (str): Text data enclosed in quotes (eg. "Hello", "Hi", 'Adi')
Booleans (bool): Represents True or False
Lists: Ordered, mutable collections (eg. [1,2,3])
Tuples: Ordered, immutable collections (eg. (1,2,3))
Sets: Unordered, collections of unique elements (eg. {1,2,3})
Dictionaries: Key-value pairs (eg. {"name": "Adi", "age": 25})
'''

